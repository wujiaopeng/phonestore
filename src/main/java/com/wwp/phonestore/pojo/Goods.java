package com.wwp.phonestore.pojo;

public class Goods {
	
	private Integer id;
	private String goodName;
	private Integer brand;
	private Integer colorId;
	private float price;
	private Integer stock;
	private String specs;
	private String cpu;
	private Integer goodState;
	private String src;

	private String brandName;
	private String colorName;
	private String goodStateName;


	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getGoodName() {
		return goodName;
	}

	public void setGoodName(String goodName) {
		this.goodName = goodName;
	}

	public Integer getBrand() {
		return brand;
	}

	public void setBrand(Integer brand) {
		this.brand = brand;
	}

	public Integer getColorId() {
		return colorId;
	}

	public void setColorId(Integer colorId) {
		this.colorId = colorId;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

	public Integer getStock() {
		return stock;
	}

	public void setStock(Integer stock) {
		this.stock = stock;
	}

	public String getSpecs() {
		return specs;
	}

	public void setSpecs(String specs) {
		this.specs = specs;
	}

	public String getCpu() {
		return cpu;
	}

	public void setCpu(String cpu) {
		this.cpu = cpu;
	}

	public Integer getGoodState() {
		return goodState;
	}

	public void setGoodState(Integer goodState) {
		this.goodState = goodState;
	}

	public String getSrc() {
		return src;
	}

	public void setSrc(String src) {
		this.src = src;
	}

	public String getBrandName() {
		return brandName;
	}

	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}

	public String getColorName() {
		return colorName;
	}

	public void setColorName(String colorName) {
		this.colorName = colorName;
	}

	public String getGoodStateName() {
		return goodStateName;
	}

	public void setGoodStateName(String goodStateName) {
		this.goodStateName = goodStateName;
	}
}
